#include "ftendianess.h"
#include <stdint.h>

void		swap_uint32(uint32_t *i)
{
	*i = (((*i >> 24) & 0xff)
				| ((*i << 8) & 0xff0000)
				| ((*i >> 8) & 0xff00)
				| ((*i << 24) & 0xff000000));
}
